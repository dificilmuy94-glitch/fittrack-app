export function getDayPlanByDate(date: Date): DayPlan | null {
  const dayOfWeek = date.getDay();
  if (dayOfWeek === 0) return null;
  return WEEKLY_PLAN[dayOfWeek - 1];
}

export function getDayPlanByKey(key: string): DayPlan | null {
  return WEEKLY_PLAN.find((d) => d.dayKey === key) ?? null;
}
